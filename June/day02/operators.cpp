#include <iostream>
using namespace std;

int main()
{
	int a = 10, b = 3;

	cout << "a + b = " << a + b << endl
	     << "a - b = " << a - b << endl
	     << "a * b = " << a * b << endl
	     << "a / b = " << a / b << endl
	     << "a % b = " << a % b << endl;

	double x = 10, y = 3;

	cout << "x/y = " << x / y << endl;

	return 0;
}
